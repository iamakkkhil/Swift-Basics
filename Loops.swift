// Loops


// For Loops
let numbers = [1,2,3,4,5,6]

for i in numbers {
    print(i*2)
}

for j in 1..<10 {
    print(j)
}


// While Loops:
// 1. while Loops
// 2. repeat while loops

// 1. While Loop
var m = 0
while m<5 {
    print("value of m is: \(m)")
    m+=1
}

// 2. repeat while loop
// same as do while loop in c++
var n=5
repeat {
    print("Hello guys i am a repeat while loop")
}while(n<2)
