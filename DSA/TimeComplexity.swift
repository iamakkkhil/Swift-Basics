// Time Complexity
/*
1. Constant Time - O(1)
2. Logarithmic Time - O(logN)
3. Linear Time - O(N)
4. Quaslinear Time - O(NlogN)
5. Quadratic Time - O(N^2)
*/


// Arrays and Dictionaries
/*
1. Array Time: 
    - Insertion at last - O(1)
    - Deletion at last - O(1)
    - Insertion at first - O(N)
    - Deletion at first - O(N)
    - Accessing elements - O(!)

2. Dictionary Time: 
    - Insertion - O(1)
    - Deletion - O(1)
*/